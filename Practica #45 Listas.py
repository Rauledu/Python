#Las listas, se utilizan para almacenar conjuntos de informacion
# de esta manera, crear una coleccion de elementos ordenados y a su vez, estos elementos
# pueden o no estar realacionados entre si, es decir una lista puede ser homogenea o heterogenea.
# 
# Lista homogenea:
# nos referimos a que todos los elementos que conforman a la lista, son del mismo tipo de dato.
# 
# listas heterogeneas:
#Nos referimos a que todos los elementos que conforman a la lista, son de diferentes tipo de datos.#

print("Lista vacia")
lista_vacia = []
print(lista_vacia)

print("\nListas Homogeneas")

vocales=['a', 'e','i', 'o', 'u']
print(vocales)

numeros_enteros = [1,2,3,4,5]
print(numeros_enteros)

numeros_decimiales = [1.5,2.2,3.3,4.9,5.0]
print(numeros_decimiales)

valores_booleanos = [True, True, False, True]
print(valores_booleanos)

print("Lista heterogenea")

datos = ["Raul", 30, True]
print(datos)

