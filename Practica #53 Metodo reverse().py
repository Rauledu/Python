#En python, al trabajar con listas en ocasiones nos podemos encontrar
# con la necesidad de invertir los elementos de una lista.
# existen diversas maneras de invertir una lista y una de ellas 
# es utilizando el metodo reverse(), el cual se encarga de invertir los elementos de una lista en 
# su mismo lugar.#

vocales = ["a","e","i","o","u"]
print(f"\nlista original: {vocales}")

vocales.reverse()
print(F"\nlista invertida: {vocales}")
print(F"lista invertida: {vocales}")
print(f"Posicion 0: {vocales[0]}")

vocales.reverse()
print(F"\nlista invertida: {vocales}")
print(F"Posicion 0: {vocales[0]}")


