#El metodo swapcase() a diferencia de los metodos lower y upper, nos permite invertir todas las letras de una cadena
# de caracteres, donde las mayusculas se convierten en minusculas y las minusculas se convierten
# en mayuscylas.#

txt_lower_upper = "Raul lUNA"
print(txt_lower_upper.swapcase())

txt_upper = "CADENA DE MAYUSCULAS"
print(txt_upper.swapcase())

txt_lower = "cadena de minusculas"
print(txt_lower.swapcase())

